    public void showProd(BancoProduto bDet){
        this.setVisible(true);
        det = new Detalhe();

        DefaultTableModel modTabProd = new DefaultTableModel();
        
        modTabProd = (DefaultTableModel) tabProd.getModel();
        
        modTabProd.setRowCount(0);
        
        for(int posLin=0;posLin < bDet.getBDDet().size();posLin++){
            det.setCod(bDet.getBDDet().get(posLin).getCod());
            det.setNome(bDet.getBDDet().get(posLin).getNome());
            det.setDescricao(bDet.getBDDet().get(posLin).getDescricao());
            det.setPreco(bDet.getBDDet().get(posLin).getPreco());
            det.setQuantidade(bDet.getBDDet().get(posLin).getQuantidade());
            modTabProd.insertRow(posLin, new Object[]{det.getCod(),det.getNome(),det.getDescricao(),det.getPreco(),det.getQuantidade()});
        }
    
    }
