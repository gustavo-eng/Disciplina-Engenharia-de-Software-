    private void tabProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabProdMouseClicked
        String nomeComponente = "";
        int posLinha = tabProd.getSelectedRow();
        for (int coluna = 0;coluna < tabProd.getColumnCount();coluna++){
            nomeComponente += tabProd.getModel().getValueAt(posLinha, coluna).toString();
            if(coluna < tabProd.getColumnCount()){
                nomeComponente += "-";
            }
        }
        
        JOptionPane.showMessageDialog(null,"Dados do Produto Inserido: "+nomeComponente,"Dados do Produto",1);
    }//GEN-LAST:event_tabProdMouseClicked

